#include <stdio.h>
/*
* Addizione es: c = a + b
* Sottrazione es c = a - b
* Moltiblicazione es c = a * b
* Divisione es c = a / b
* Modulo es c = 11 % 2 (c = 1 di resto)
* Incremento a++ e' un esemplificazione di a = a + 1 
* Decremento a-- e' un esemplificazione di a = a - 1
*/


int main(){
    int a = 0;
    a++;
    printf("Valore di a = %d\n", a);
    a++;
    printf("Valore di a = %d\n", a);
}

